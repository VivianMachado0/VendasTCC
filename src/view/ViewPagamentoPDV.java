    //Calcula o valor a pagar e o troco
    private void calcularPagamento() {

        float subTotal, desconto, recebido, pagar, troco;

        try {
            subTotal = Float.parseFloat(jtfSubTotal.getText());
        } catch (Exception e) {
            subTotal = 0;
        }

        try {
            desconto = Float.parseFloat(jtfDesconto.getText());
        } catch (Exception e) {
            desconto = 0;
            jtfDesconto.setText("0"); //Se me der uma execção ele vai jogar o valor zero.
        }

        try {
            recebido = Float.parseFloat(jtfRecebido.getText());
        } catch (Exception e) {
            recebido = 0;
            jtfRecebido.setText("0");//Se me der uma execção ele vai jogar o valor zero.
        }

        //Calcular o valor a pagar
        pagar = subTotal - desconto;
        jlValorTotal.setText(blm.arredondamentoComPontoDuasCasasString(pagar)); // esses simbolos é para concactenar a mesma para string(+"")
        //Calculando o troco
        troco = recebido - pagar;
        jtfTroco.setText(blm.arredondamentoComPontoDuasCasasString(troco));

    }

