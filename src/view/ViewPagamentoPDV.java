    /**
     * Preencher o Combobox em todas as formas de pagamento
     */
    private void listaFormaPagamento() {
        listaModelFormaPagamento = getControllerFormaPagamento().getListaFormaPagamentoController();
        jcbPagamento.removeAllItems();
        for (int i = 0; i < listaModelFormaPagamento.size(); i++) {
            jcbPagamento.addItem(listaModelFormaPagamento.get(i).getForPagDescricao());
        }
    }
    
